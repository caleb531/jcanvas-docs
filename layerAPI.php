<h2 class='menu'>Layer API</h2>

<p>The jCanvas Layer API provides each canvas with a set of layers that you create. These layers provide a higher degree of interactivity on the canvas.</p>

<p>Each layer can contain only one drawing (rectangle, arc, image, <em>etc.</em>). This is because each drawing <em>is</em> the layer object itself.</p>

