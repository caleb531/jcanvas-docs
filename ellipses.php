<h2>Ellipses</h2>

<p>You can draw an ellipse using the same basic properties used to <a href='/projects/jcanvas/docs/rectangles/'>draw a rectangle</a>.</p>

<pre class="prettyprint lang-js demo">
$("canvas").drawEllipse({
  fillStyle: "#c33",
  x: 150, y: 100,
  width: 200, height: 100
});
</pre>

<pre class="prettyprint lang-js demo">
$("canvas").drawEllipse({
  strokeStyle: "#36c",
  strokeWidth: 4,
  x: 150, y: 100,
  width: 200, height: 100
});
</pre>