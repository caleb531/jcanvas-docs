<h2>Restore Canvas</h2>

<p>The <code>restoreCanvas()</code> method restores the last saved state of the canvas. The method usually works in conjunction with the <code><a href="/projects/jcanvas/docs/saveCanvas/">saveCanvas()</a></code> method.</p>

<pre class="prettyprint lang-js">
$("canvas").restoreCanvas();
</pre>